package model;

public class Fila {
	
	private String NovoProjeto;
	private String ManutencaoSV;
	private String ManutencaoSERP;
	private String Servidore;
	private String Rede;
	private int Telefonia;
	private String Desktop;
	
	public String getNovoProjeto() {
		return NovoProjeto;
	}
	public void setNovoProjeto(String novoProjeto) {
		NovoProjeto = novoProjeto;
	}
	public String getManutencaoSV() {
		return ManutencaoSV;
	}
	public void setManutencaoSV(String manutencaoSV) {
		ManutencaoSV = manutencaoSV;
	}
	public String getManutencaoSERP() {
		return ManutencaoSERP;
	}
	public void setManutencaoSERP(String manutencaoSERP) {
		ManutencaoSERP = manutencaoSERP;
	}
	public String getServidore() {
		return Servidore;
	}
	public void setServidore(String servidore) {
		Servidore = servidore;
	}
	public String getRede() {
		return Rede;
	}
	public void setRede(String rede) {
		Rede = rede;
	}
	public int getTelefonia() {
		return Telefonia;
	}
	public void setTelefonia(int telefonia) {
		Telefonia = telefonia;
	}
	public String getDesktop() {
		return Desktop;
	}
	public void setDesktop(String desktop) {
		Desktop = desktop;
	}

	
	
}
